# Sobre string

# Definindo minha string
minha_string = "Abacaxi"

# Primeira letra da minha string
letra_1 = minha_string[0]
print(letra_1)

# Caixa alta
string_alta = minha_string.upper()
print(string_alta)

# Caixa baixa
string_baixa = minha_string.lower()
print(string_baixa)

# Operação de soma
fruta_1 = "abacaxi"
fruta_2 = "pera"
duas_frutas = fruta_1 + " e " + fruta_2
print(duas_frutas)

nome = "Téo"
sobrenome = "Calvo"
nome_completo = nome + " " + sobrenome
